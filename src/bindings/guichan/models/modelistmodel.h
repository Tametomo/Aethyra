#ifndef MODE_LIST_MODEL_H
#define MODE_LIST_MODEL_H

#include <guichan/listmodel.hpp>

#include <string>
#include <vector>

/**
 * The list model for mode list.
 *
 * \ingroup Interface
 */
class ModeListModel : public gcn::ListModel
{
    public:
        /**
         * Constructor.
         */
        ModeListModel();

        /**
         * Destructor.
         */
        virtual ~ModeListModel() { }

        /**
         * Returns the number of elements in container.
         */
        int getNumberOfElements() { return mVideoModes.size(); }

        /**
         * Returns element from container.
         */
        std::string getElementAt(int i) { return (i < 0 || i >= (int) mVideoModes.size()) ? "" : mVideoModes[i]; }

        /**
         * Finds a specific mode. -1 if not found.
         */
        int find(std::string mode);

    private:
        std::vector<std::string> mVideoModes;
};

#endif
